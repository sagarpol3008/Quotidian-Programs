#include<iostream>
using namespace std;
int main(){
    int i;
    float f;
    double d;
    char c;
    cout << "Size of Integer is: " << sizeof(i) << endl;
    cout << "Size of Float is: " << sizeof(f) << endl;
    cout << "Size of Double is: " << sizeof(d) << endl;
    cout << "Size of Character is: " << sizeof(c) << endl;


    return 0;
}
